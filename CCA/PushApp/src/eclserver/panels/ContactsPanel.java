private void btnDeleteContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteContactActionPerformed
// TODO add your handling code here:
    
     try{
            int id = contactsList.getSelectedIndex();

            if (id != -1) {
                fireActionEvent(evt);
            } else {
                JOptionPane.showMessageDialog(this, "Select a list item to delete.");
            }
       
        }catch (Exception ex){
             JOptionPane.showMessageDialog(this, "Delete Failed: " + ex.getMessage());
         }
    
    
}//GEN-LAST:event_btnDeleteContactActionPerformed

private void btnSaveContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveContactActionPerformed
// TODO add your handling code here:

    try{
     //   System.out.println("Validating required fields...");
       if ((contactPanel.getGroupName().length() > 0 ) && (contactPanel.getLastName().length() > 0 )
               && (contactPanel.getFirstName().length() > 0 ) && (contactPanel.getEmail().length() > 0)) {
      
            fireActionEvent(evt);
        } else {
           JOptionPane.showMessageDialog(this, "Complete the Mandatory Fields in RED");
        }
    }catch (Exception ex){
        System.out.println("Error Saving Contact:" + ex.getMessage());
    }

}//GEN-LAST:event_btnSaveContactActionPerformed

