private void contactsListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_contactsListValueChanged
// TODO add your handling code here:

    if (evt.getValueIsAdjusting() == false) {
        
        JList entryList = (JList) evt.getSource();
       // System.out.println("List item selected: " + entryList.getSelectedIndex());
        selectedEntry = entryList.getSelectedIndex();
        int selList = contactsList.setSelectedIndex(selectedEntry);
    //    System.out.println("getting selList " + selList);
            
        if (contactsList.getSelectedIndex() == -1) {
            //No selection, disable fire button.
                btnDeleteContact.setEnabled(false);

            } else {
            //Selection, enable the fire button.\
            //    setListSelection(contactsList.getSelectedIndex());
            //    System.out.println("Clicked "  + contactsList.getSelectedListEntry().getId() + 
         //               " LastName: " + contactsList.getSelectedListEntry().getLastName()); 
                
                contactPanel.setAddress(contactsList.getSelectedListEntry());
                contactPanel.setEditable(true);
                btnDeleteContact.setEnabled(true);
                btnSaveContact.setEnabled(true);
                btnAddContact.setEnabled(false);
                
            }
        }



}//GEN-LAST:event_contactsListValueChanged




  private void fireActionEvent(ActionEvent evt) {
        ActionListener[] listenerList = listeners.getListeners(ActionListener.class);

        for (int i = listenerList.length-1; i>=0; --i) {
            listenerList[i].actionPerformed(evt);
        }

    }

    public void addActionListener(ActionListener listener) {
        listeners.add(ActionListener.class, listener);
    }

    public void removeActionListener(ActionListener listener) {
        if (listeners != null) {
            listeners.remove(ActionListener.class, listener);
        }
    }

    public void setContactsListEntries(List<AddressObject> objects){
        contactsList.addListEntries(objects);
    }
    
    public AddressObject getSelectedListEntry(){
        return contactsList.getSelectedListEntry();
    }
    
    public void addContactsListEntry(AddressObject object){
        contactsList.addListEntry(object);
    }
    
    public AddressObject getPanelEntry(){
        return contactPanel.getAddress();
    }
    
    public void removeListEntry(){
        contactsList.deleteSelectedEntry();
    }
    
    public void removeAllEntries(){
        contactsList.deleteAllEntries();
    }
            
    
    public void updateListEntry(AddressObject obj, int value) {
        contactsList.updateIndexEntryAt(obj, value);
    }
    
    public void clearPanel(){
        contactPanel.clear();
    }
    
    public void resetButtons(){
        btnAddContact.setEnabled(true);
        btnSaveContact.setEnabled(false);
        btnDeleteContact.setEnabled(false);
        contactPanel.setEnabled(false);
        contactPanel.setEditable(false);
            
    }
    
    public void setListSelection(int value){
        this.selectedEntry = value;
    } 
    
    public int getListSelection(){
        return selectedEntry;
    }

    

